package Basic;

public class SwitchCaseString {

	public static void main(String[] args) {
		
		String name = "Ram";
		
		switch(name)
		{
		case "Lakhan":
			System.out.println("hey Lakhan");
			break;
			
		case "Rambo":
			System.out.println("Hey Rambo");
			break;
			
		case "Ram":
			System.out.println("Hey Ram");
			break;
			
		default:
			System.out.println("No all");
		}

	}

}
