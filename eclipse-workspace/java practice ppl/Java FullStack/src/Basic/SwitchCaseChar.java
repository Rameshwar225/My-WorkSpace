package Basic;

public class SwitchCaseChar {

	public static void main(String[] args) {
		
		char x = 'A';
		
		switch(x)
		{
		case 'A':
			System.out.println("its A");
			break;
		
		case 'B':
			System.out.println("Its B");
			break;
			
		case 'C':
			System.out.println("its C");
			break;
			
		default:
			System.out.println("its out of these");
		}

	}

}
