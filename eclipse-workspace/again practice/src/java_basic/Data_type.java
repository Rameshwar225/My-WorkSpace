package java_basic;

public class Data_type {
	public static void main(String[] arg)
	{
		System.out.println("primitive data types in java\n");
		System.out.println("1.Int");
		System.out.println("2.byte");
		System.out.println("3.float");
		System.out.println("4.double");
		System.out.println("5.char");
		System.out.println("6.long");
		System.out.println("7.Boolean");
		
		System.out.println("lets understand through example: ");
		int a=5;
		byte b=2;
		char c ='R';
		boolean d = true;
		float e = 21.6f;
		double a1 = 56.789;
		long l = 9665776869l;
		
		System.out.println("the value of int a is: "+a);
		System.out.println("the value of byte b is: "+b);
		System.out.println("the value of char c is: "+c);
		System.out.println("the value of boolean is: "+d);
		System.out.println("the value of float is: "+e);
		System.out.println("the value of double is: "+a1);
		System.out.println("the value of long is: "+l);
	}

}
